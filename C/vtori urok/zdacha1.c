/*Направете object-like макро, което да улеснява дефинирането на масив
от 5 елемента, и което да се използва за да се принтират елементите от
масива. Идеята е чрез макрото да контролирате размера на масива.*/
#include <stdio.h>
#define ARR_SIZE 5
int main(void){
    int arr[ARR_SIZE]={1,2,3,4,5};
    for (int i = 0; i < ARR_SIZE; i++){
        printf ("%d ", arr[i]);
    }
    return 0;
}